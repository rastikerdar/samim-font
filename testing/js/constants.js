function bulkChangeTextForTab(grumpy, value, except) {
	for (var i = 0; i < grumpy.columns.length; i++) {
		var x = $(grumpy.tab).find(grumpy.columns[i].innerblock);
		var z = x.find('.textline').not(except);
		
		z.text(value);
	}
}


